<h1 class="p-4 text-[48px] font-bold">Lesson 10 : Its vs. It's, 'Tis</h1>
<p>
	<span class="underline italic font-semibold">" Its " </span>

    is the possessive of the pronoun "it." Note that there is no apostrophe. <br>
	Example: Its appearance was misleading.

</p>

<br>
<p>
	<span class="underline italic font-semibold">" It's " </span>
     is a contraction meaning "it is."  <br>
	Example: It's a long way to Tipperary. "It's" can also be a contraction meaning "it has." <br>
	Example: It's been a long day.

</p>
<br>
<p>
	<span class="underline italic font-semibold">" 'Tis "</span>
     is also a contraction meaning "it is." <br>
	Example: 'Tis seldom used in modern English.
</p>


