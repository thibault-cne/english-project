<h1 class="p-4 text-[48px] font-bold">Lesson 9 : Lie, Lying, Lay, Lain vs. Lie, Lying, Lied, Lied</h1>
<p>
	<span class="underline italic font-semibold">Lie, Lying, Lay, Lain :</span>

"Lie" is an intransitive verb meaning "to rest" or "to be at rest." Its forms are: lie, lying, lay, lain. 

</p>

<br>

<p>
	<span class="underline italic font-semibold">Examples :</span>
<br>
1) Lie down. <br>
2) Lying in the sun dries our skin. <br>
3) The parcels lay on the table. <br>
4) We have lain in the sun for thirty minutes. <br>

</p>
	<br>
<p>
	<span class="underline italic font-semibold">Lie, Lying, Lied, Lied :</span>

	"Lie" can also mean make an untrue statement. It is an intransitive verb whose forms are: 'lie' and lying (present), 'lied' (both past and participle.) 
</p>

<br>

<p>
	<span class="underline italic font-semibold">Examples :</span>
	<br>
	1) Please don't lie to me. <br>
	2) He was punished for lying. <br>
	3) They lied to their parents. <br>
	4) They have lied before. <br>
</p>





